def _section_header(pdf: FPDF, text: str) -> None:
    pdf.set_fill_color(*_C_SECTION_BG)
    pdf.set_font("Helvetica", "B", 10)
    pdf.set_text_color(*_C_TEXT)
    pdf.set_draw_color(*_C_BORDER)
    pdf.cell(0, 8, f"  {text}", border="B", fill=True, new_x=XPos.LMARGIN, new_y=YPos.NEXT)
    pdf.ln(2)


def _kv_row(pdf: FPDF, key: str, value: str, label_w: float = 45) -> None:
    pdf.set_font("Helvetica", "B", 8)
    pdf.set_text_color(*_C_MUTED)
    pdf.cell(label_w, 6, key, new_x=XPos.RIGHT, new_y=YPos.TOP)
    pdf.set_font("Helvetica", "", 8)
    pdf.set_text_color(*_C_TEXT)
    pdf.cell(0, 6, _t(value or "-"), new_x=XPos.LMARGIN, new_y=YPos.NEXT)


def _sev_badge(pdf: FPDF, severity: str, x: float, y: float, w: float = 18, h: float = 5) -> None:
    color = _SEV_COLORS.get(severity.lower(), _C_MUTED)
    r, g, b = color
    # Light background: blend toward white at 15%
    bg = (int(r + (255 - r) * 0.85), int(g + (255 - g) * 0.85), int(b + (255 - b) * 0.85))
    pdf.set_fill_color(*bg)
    pdf.set_draw_color(*bg)
    pdf.set_xy(x, y)
    pdf.cell(w, h, severity.upper(), align="C", fill=True, new_x=XPos.RIGHT, new_y=YPos.TOP)
    pdf.set_text_color(*color)
    pdf.set_xy(x, y)
    pdf.set_font("Helvetica", "B", 6)
    pdf.cell(w, h, severity.upper(), align="C", new_x=XPos.RIGHT, new_y=YPos.TOP)
    pdf.set_text_color(*_C_TEXT)


def _status_badge(pdf: FPDF, status: str, x: float, y: float, w: float = 16, h: float = 5) -> None:
    label_map = {
        "pass": "PASS", "fail": "FAIL", "not_applicable": "N/A",
        "manual_check": "MANUAL", "error": "ERROR",
    }
    color_map = {
        "pass": _C_PASS, "fail": _C_FAIL, "not_applicable": _C_NA,
        "manual_check": _C_MANUAL, "error": (124, 58, 237),
    }
    color = color_map.get(status, _C_MUTED)
    r, g, b = color
    bg = (int(r + (255 - r) * 0.85), int(g + (255 - g) * 0.85), int(b + (255 - b) * 0.85))
    pdf.set_fill_color(*bg)
    pdf.set_draw_color(*bg)
    pdf.set_xy(x, y)
    pdf.cell(w, h, "", fill=True, new_x=XPos.RIGHT, new_y=YPos.TOP)
    pdf.set_text_color(*color)
    pdf.set_xy(x, y)
    pdf.set_font("Helvetica", "B", 6)
    pdf.cell(w, h, label_map.get(status, status.upper()), align="C", new_x=XPos.RIGHT, new_y=YPos.TOP)
    pdf.set_text_color(*_C_TEXT)


