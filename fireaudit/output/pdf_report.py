def render_pdf(report: dict, output_path: Path | None = None) -> bytes:
    """Render *report* as a PDF and return the raw bytes.

    If *output_path* is given, the PDF is also written to that path.
    """
    pdf = _FireAuditPDF(report)
    pdf.add_page()

    # Title block (below header)
    pdf.set_font("Helvetica", "B", 16)
    pdf.set_text_color(*_C_TEXT)
    device = report.get("device", {})
    hostname = device.get("hostname") or device.get("vendor", "Unknown")
    pdf.cell(0, 10, _t(f"Security Audit Report - {hostname}"), new_x=XPos.LMARGIN, new_y=YPos.NEXT)
    pdf.set_font("Helvetica", "", 8)
    pdf.set_text_color(*_C_MUTED)
    pdf.cell(0, 6, _t(f"Generated by FireAudit  |  {report.get('generated_at', '')}"),
             new_x=XPos.LMARGIN, new_y=YPos.NEXT)
    pdf.ln(4)

    _write_posture_banner(pdf, report)
    _write_device_info(pdf, report)
    _write_summary_stats(pdf, report)
    _write_framework_scores(pdf, report)

    if report.get("findings"):
        pdf.add_page()
        _write_findings(pdf, report)

    _write_manual_checks(pdf, report)

    raw = bytes(pdf.output())

    if output_path is not None:
        Path(output_path).write_bytes(raw)

    return raw
