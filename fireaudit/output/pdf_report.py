class _FireAuditPDF(FPDF):
    """Custom FPDF subclass with FireAudit header/footer."""

    def __init__(self, report: dict) -> None:
        super().__init__(orientation="P", unit="mm", format="A4")
        self._report = report
        self.set_auto_page_break(auto=True, margin=20)
        self.set_margins(left=15, top=15, right=15)

    def header(self) -> None:
        device = self._report.get("device", {})
        hostname = device.get("hostname") or device.get("vendor", "Unknown")
        vendor = device.get("vendor", "")
        self.set_fill_color(*_C_HEADER_BG)
        self.rect(0, 0, 210, 14, "F")
        self.set_font("Helvetica", "B", 9)
        self.set_text_color(*_C_WHITE)
        self.set_xy(15, 3)
        self.cell(130, 8, _t(f"FireAudit - {hostname} ({vendor.upper()})"), new_x=XPos.RIGHT, new_y=YPos.TOP)
        self.set_font("Helvetica", "", 8)
        self.set_text_color(148, 163, 184)
        self.set_xy(150, 3)
        self.cell(45, 8, f"Page {self.page_no()}", align="R", new_x=XPos.RIGHT, new_y=YPos.TOP)
        self.set_text_color(*_C_TEXT)
        self.ln(5)

    def footer(self) -> None:
        self.set_y(-12)
        self.set_font("Helvetica", "", 7)
        self.set_text_color(*_C_MUTED)
        ts = self._report.get("generated_at", "")
        self.cell(0, 8, f"Generated by FireAudit | {ts}", align="C")
        self.set_text_color(*_C_TEXT)


