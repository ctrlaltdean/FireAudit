"""PDF report generator using fpdf2 (pure Python, no system dependencies)."""

from __future__ import annotations

from pathlib import Path
from typing import Any

from fpdf import FPDF, XPos, YPos

# ---------------------------------------------------------------------------
# Text helpers
# ---------------------------------------------------------------------------

def _t(text: object, maxlen: int = 0) -> str:
    """Convert to string and strip non-latin-1 characters (core fonts limitation).

    Replaces em-dash/en-dash with hyphens, then drops any remaining
    characters outside the latin-1 range so fpdf2 doesn't raise.
    """
    s = str(text) if text is not None else ""
    s = s.replace("\u2014", "-").replace("\u2013", "-").replace("\u2019", "'").replace("\u2018", "'")
    s = s.encode("latin-1", errors="ignore").decode("latin-1")
    if maxlen and len(s) > maxlen:
        s = s[:maxlen - 1] + "+"
    return s


# ---------------------------------------------------------------------------
# Colour palette (RGB tuples)
# ---------------------------------------------------------------------------
_C_PAGE_BG = (248, 250, 252)
_C_HEADER_BG = (30, 41, 59)
_C_WHITE = (255, 255, 255)
_C_SECTION_BG = (241, 245, 249)
_C_BORDER = (226, 232, 240)
_C_TEXT = (30, 41, 59)
_C_MUTED = (100, 116, 139)

_C_CRITICAL = (220, 38, 38)
_C_HIGH = (234, 88, 12)
_C_MEDIUM = (217, 119, 6)
_C_LOW = (101, 163, 13)
_C_PASS = (22, 163, 74)
_C_FAIL = (220, 38, 38)
_C_NA = (100, 116, 139)
_C_MANUAL = (133, 77, 14)
_C_CMD_BG = (240, 253, 244)
_C_CMD_BORDER = (22, 163, 74)

_SEV_COLORS: dict[str, tuple] = {
    "critical": _C_CRITICAL,
    "high": _C_HIGH,
    "medium": _C_MEDIUM,
    "low": _C_LOW,
    "info": (2, 132, 199),
}

_GRADE_COLORS: dict[str, tuple] = {
    "A": _C_PASS,
    "B": (101, 163, 13),
    "C": _C_MEDIUM,
    "D": _C_HIGH,
    "F": _C_CRITICAL,
}


